---@field opts? table|fun(self:table, opts:table):table
---@field keys? string|string[]|LazyKeysSpec[]
---@field cmd? string|string[]
---@field ft? string|string[]
---@field event? string|string[]
---@field lazy? boolean
---@field priority? number
